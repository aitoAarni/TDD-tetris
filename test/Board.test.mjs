import { beforeEach, describe, test } from "vitest";
import { expect } from "chai";
import { Board } from "../src/Board";

describe("Board can be created from a string", () => {
  let board;
  beforeEach(() => {
    board = new Board(6, 6);
  });
  test("Created from a string", () => {
    board.setBoard(
      `......
            ......
            ......
            ......
            TTT...
            .T....`
    );
    expect(board.toString()).to.equalShape(`
                ......
            ......
            ......
            ......
            TTT...
            .T....
                `);
  });
});
